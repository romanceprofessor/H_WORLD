
public class My_Fruits {
	
	String fruitName;
	String fruitFlavor;
	
	My_Fruits(String fruitName, String fruitFlavor) {
		this.fruitName = fruitName;
		this.fruitFlavor = fruitFlavor;
	}
	
	void setItsName(String name) {
		fruitName = name;
	}
	
	String getItsName() {
		return fruitName;
	}
	
	void setItsFlavor(String flavor) {
		fruitFlavor = flavor;
	}
	
	String getItsFlavor() {
		return fruitFlavor;
	}
	
	
}
